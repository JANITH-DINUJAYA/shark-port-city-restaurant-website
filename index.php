document.addEventListener('DOMContentLoaded', () => {


  // --- Tab switching ---
  const menusTab = document.getElementById('menus-tab');
  const promoTab = document.getElementById('promo-tab');
  const menusContent = document.getElementById('menus-content');
  const promoContent = document.getElementById('promo-content');

  function switchTab(tab) {
    if (!menusContent || !promoContent) return;
    if (tab === 'menus') {
      menusContent.classList.add('active-content');
      promoContent.classList.remove('active-content');
      menusTab.classList.add('active');
      promoTab.classList.remove('active');
    } else {
      promoContent.classList.add('active-content');
      menusContent.classList.remove('active-content');
      promoTab.classList.add('active');
      menusTab.classList.remove('active');
    }
  }

  if (menusTab) menusTab.addEventListener('click', (e) => { e.preventDefault(); switchTab('menus'); });
  if (promoTab) promoTab.addEventListener('click', (e) => { e.preventDefault(); switchTab('promo'); });

});

// --- Add to Cart ---
function addToCart(button) {
    
  const item = button.closest('.menu-item');
   if (item.classList.contains('promo-card')) return;
  const itemName = item.querySelector('h4').getAttribute('data-name');
  const itemPrice = parseFloat(item.querySelector('.price').getAttribute('data-price'));
   const itemImage = item.querySelector('img').getAttribute('src');
  const newItem = {
    name: itemName,
    price: itemPrice,
     image: itemImage,
    quantity: 1
  };

  let cart = JSON.parse(localStorage.getItem('cart')) || [];

  const existingItem = cart.find(cartItem => cartItem.name === newItem.name);

  if (existingItem) {
    existingItem.quantity++;
  } else {
    cart.push(newItem);
  }

  localStorage.setItem('cart', JSON.stringify(cart));
  alert(`${newItem.name} added to cart!`);
   updateCartCount();
}
function updateCartCount() {
  const cart = JSON.parse(localStorage.getItem('cart')) || [];
  const count = cart.reduce((total, item) => total + item.quantity, 0);
  const cartCountEl = document.getElementById('cart-count');
  if (cartCountEl) {
    cartCountEl.textContent = count;
  }
}
document.addEventListener('DOMContentLoaded', () => {
    updateCartCount(); // <-- updates cart badge on page load
});
// --- Promo Code Validation ---
document.addEventListener('DOMContentLoaded', () => {
    const promoCards = document.querySelectorAll('.promo-card');

    promoCards.forEach(card => {
        const applyBtn = card.querySelector('.apply-btn');
        const input = card.querySelector('.promo-input');
        const addCartBtn = card.querySelector('.add-cart-btn');
        const priceEl = card.querySelector('.price');
        const promoId = card.getAttribute('data-promo-id');

        if (addCartBtn) addCartBtn.style.display = 'none'; // hide initially

        // Apply Promo Code
        if (applyBtn) {
            applyBtn.addEventListener('click', async () => {
                const userCode = input.value.trim();
                if (!userCode) {
                    alert('Enter a promo code!');
                    return;
                }

                try {
                    const res = await fetch(`getPromoCode.php?promoId=${promoId}&code=${encodeURIComponent(userCode)}`);
                    const data = await res.json();

                    if (data.validCode && data.validCode.status === 'Active') {
                        const discountPercent = parseFloat(data.validCode.discount) || 0;
                        const originalPrice = parseFloat(priceEl.getAttribute('data-price'));
                        const discountedPrice = originalPrice * (1 - discountPercent / 100);

                        // Update price display and store numeric value
                        priceEl.textContent = `Rs. ${discountedPrice.toFixed(2)} (Discount ${discountPercent}%)`;
                        priceEl.setAttribute('data-discounted-price', discountedPrice);

                        applyBtn.style.display = 'none';
                        addCartBtn.style.display = 'inline-block';
                        addCartBtn.disabled = false;
                    } else {
                        alert('Invalid or inactive promo code!');
                        addCartBtn.disabled = true;
                    }
                } catch (err) {
                    console.error(err);
                    alert('Error checking promo code.');
                }
            });
        }

        // Add to Cart
        if (addCartBtn) {
            addCartBtn.addEventListener('click', () => {
                const itemNameEl = card.querySelector('h4');
                if (!itemNameEl) return;

                const itemName = itemNameEl.textContent;
                const priceEl = card.querySelector('.price');
                const itemPrice = parseFloat(priceEl.getAttribute('data-discounted-price') || priceEl.getAttribute('data-price'));
                const itemImage = card.querySelector('img')?.src || '';

                const newItem = {
                    name: itemName,
                    price: itemPrice,
                    image: itemImage,
                    quantity: 1
                };

                let cart = JSON.parse(localStorage.getItem('cart')) || [];
                const existingItem = cart.find(cartItem => cartItem.name === newItem.name);

                if (existingItem) {
                    existingItem.quantity++;
                } else {
                    cart.push(newItem);
                }

                localStorage.setItem('cart', JSON.stringify(cart));
                alert(`${newItem.name} added to cart!`);
                updateCartCount();
            });
        }
    });
});


